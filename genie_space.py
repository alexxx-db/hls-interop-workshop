# MAGIC ## Create Genie Space

# COMMAND ----------

# DBTITLE 1,Create Genie Space via SDK
from databricks.sdk import WorkspaceClient

w = WorkspaceClient()

space_name = "Healthcare Interoperability Workshop"

tables = [
    # CCDA
    f"{catalog}.ccda.ccda_claims_raw",
    f"{catalog}.ccda.medications",
    # FHIR/OMOP
    f"{catalog}.fhir.person",
    f"{catalog}.fhir.condition_occurrence",
    f"{catalog}.fhir.procedure_occurrence",
    f"{catalog}.fhir.visit_occurrence",
    f"{catalog}.fhir.condition_features",
    # HL7
    f"{catalog}.hl7.parsed_hl7",
    # Audit
    f"{catalog}.audit.jdbc_ingests",
]

try:
    space = w.genie.create_space(
        title=space_name,
        description=GENIE_INSTRUCTIONS,
        table_identifiers=tables,
    )
    print(f"Genie space created: {space_name}")
    print(f"Space ID: {space.space_id}")
    print(f"Open at: #/genie/spaces/{space.space_id}")
except Exception as e:
    print(f"Could not create Genie space programmatically: {e}")
    print(f"\nCreate it manually:")
    print(f"  1. SQL > Genie Spaces > New")
    print(f"  2. Name: {space_name}")
    print(f"  3. Attach tables: {', '.join(tables)}")
    print(f"  4. Paste the instructions prompt from the cell above")

